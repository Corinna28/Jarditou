var age = prompt('Quel est votre âge ?');

if (0 <= age && age <= 20) {
    alert("Vous êtes un jeune");
}

else if (21 <= age && age <= 40) {
    alert("Vous êtes un adulte");
    
}

else if (41 <= age && age <= 100) {
    alert("Vous êtes un Vieux");
}

else (100 <= age)
{
    alert("Vous êtes Centenaire");
   
} 
